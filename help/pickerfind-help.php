    <h2><strong>Picker Finder help</strong></h2>
    <p>All volunteers are assigned a unique identification number. Some forms require the number to be entered instead of the name. The picker finder can either look up a name from a number, or look up a number from a name.</p>
    <p>If you want to find out the name that is assigned a particular number, type the number in the green space and press 'Enter'. The name will appear below.</p>
    <p>If you want to find the number that is assigned to a name, type part of the last name in the green space and press 'Enter'. All names in the database that match that name will be be listed with their numbers. You can type a full last name or just a few letters and all matches will be shown.</p>
    <p>You can also search by first name by typing a space and then any number of letters from the start of the first name.</p>
    <p> You can search by eamil address by typing a dash ('-') as the first character</p>
    <p> You can decode an encrypted picker ID by typing a '+' first, or encode a picker ID by typing a period ('.')
    <p>The table at the bottom lists the most commonly used  names and numbers.</p>
