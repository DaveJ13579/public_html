        <h2><strong>Calculator help</strong></h2>
            <p>The calculator computes the numeric value of algebraic expressions entered into the form text area at the top. Expressions can be entered using the keypad in the calculator or by clicking in the text area and typing. As an algebraic calculator it allows the use of parentheses. There is a memory storage that's contents are shown below the memory buttons. The memory is accessed with the following buttons: </p>
            <ul>
              <li><strong>MC</strong>: Clear memory</li>
              <li><strong>MR</strong>: Retrieve the memory value to the main text area</li>
              <li><strong>MS</strong>: Switch the memory with the value in the text area</li>
              <li><strong>M+</strong>: Add the current value to the memory</li>
              <li><strong>M-</strong>: Subtract the current value from the  memory</li>
            </ul>
<p>Typing the 'Tab' key will put the cursor into the main window if keyboard entry is preferred. Then 'Enter' will produce the result.</p>
<p>If characters other than 0-9, (,),/,*,+,-,p,o,w and E are entered, the message 'Illegal character' will be displayed.</p>
            <p>If an undecipherable expression with valid characters is entered, a question mark will be added to the expression.</p>
      <p>There is no key entry for raising a number to a power (squaring, square root, etc.). However, the keyboard can be used to type the following expression: pow(base, exponent) to be calculated. For instance, the square root of 3 would be entered as pow(3,.5)  </p>
            <p>Scientific notation may be used in expressions, for example, 3.14E3 (three point one four times ten to the third power.)</p>
            <p>Clicking on 'More' in the title bar opens serveral rows of conversion utilities. Clicking on these will convert the number in the main window to the units indicated.</p>
