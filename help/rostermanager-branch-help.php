    <h2><strong>Harvest roster manager</strong></h2>

    <p>This page is for inserting roster rows or changing roster rows in the rosters table. </p>
<p>When this page loads from <strong>Harvest Update</strong> or the <strong>Branch Leader Home</strong> the roster for that harvest will already be loaded into the form.</p>
<p>The page shows the specifics about the harvest to ensure that the correct harvest is shown. </p>
<p>The first line of the form is for adding a volunteer to the roster. Select the name from the drop-down menu. Select whether the volunteer was a leader or harvested from the Status field. Then click on <strong>Insert into roster</strong>.</p>
<p>The volunteer will appear in the roster list below. </p>
<p>The bottom section is for displaying and changing information in an existing roster. Status can be changed and the click on <strong>Update roster</strong>. If you leave the page before clicking on Update roster, any changes will be lost. </p>
<p>To delete a roster row that is incorrect, select <strong>Delete</strong> from the <strong>Status</strong> drop-down menu and then click <strong>Update roster</strong>. The row will disappear. </p>
