    <h2><strong>Harvest update</strong>    </h2>
    <p>This page is for changing details about an existing harvest. You may get here either at once after adding a new harvest, or by clicking in the second column of the list on the Branch leaders home page. </p>
    <p>Most of the form fields are the same as on the &quot;Add a new harvest&quot; page. </p>
    <p>The only new form fields are:</p>
    <p><strong>Where donated</strong> - The food agency, where the produce  was donated or delivered. NOTE: This should be filled in ONLY when the place delivered-to is not on the dropdown menu on the Distributions page and cannot be added. Staff with suffucient database access level can see this and add the agency to the dropdown, assign the distribution there and, last, remove the text from this field.</p>
    <p><strong>Crops and Pounds</strong>: There are spaces for 10 different types of crops. Select the crops from the drop-down lists. You must select at least one crop if the harvest is to appear on the public Harvests page. Before the harvest, leave the space for the pounds of that crop blank. After the harvest fill in the number of pounds of each crop that you picked. </p>
    <p>The other fields are: </p>
    <p><strong>Leader</strong>: this drop-down list has all of the volunteers who are approved as harvest leaders.<br />
		<br />
		<strong>Date</strong>: Clicking on the space makes a calendar appear. You can select the day of the harvest from the calendar, or just type it in. Harvest dates must be in &quot;yyyy-mm-dd&quot; format. 
    Note that if you leave the date blank, the page will automatically give the harvest the dummy date &quot;yyyy-00-00&quot; where yyyy is the current year. This will make the harvest appear on your harvest list for the current year. You may want to do this when you know a harvest will happen but you do not yet know the exact date.<br />
		<br />
		<strong>Harvest time</strong>: Select the time of the harvest from the drop-down list.<br />
		<br />
		<strong>Co-leader</strong>: Select a co-leader from the drop-down list if there is one.<br />
		<br />
		<strong>Pickers needed</strong>: This is the total number of volunteers that are needed at the harvest, including the leader and co-leader. <br />
		<br />
		<strong>Duration</strong>: How long the harvest is expected to take. <br />
		<br />
		<strong>Type</strong>: Harvests are categoreized by type. Select from the list.<br />
		<br />
		<strong>Status</strong>:<br />
		</p>
    <blockquote> <strong>closed</strong>: The harvest will be added to the list but it will not appear on the public Harvests page.<br />
    	<strong>open</strong>: The harvest will appear on the public harvests page. But it must have a date, a time, a duration and a leader.<br />
    	<strong>unscheduled</strong>: The harvest is on the list for the year but does not yet have a date. </blockquote>
		<strong>Pre-signup info</strong>: This is a short description of the harvest that will appear on the public Harvests page. It should be only information that might be helpful for volunteers to decide to sign up or not.<br />
		<br />
	 <strong>Post-signup info</strong>: This information will show up on the page of details that volunteers see after they sign up for the harvest. It might include things like special equipment or directions to the site when it is not clear from Google Maps.    </div>
