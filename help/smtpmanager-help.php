    <h2><strong>SMTP manager help</strong></h2>
    <p>This page is used for managing the email addresses that are used as the 'from' address in automatic emails that are sent by various pages such as when volunteers sign up for harvests. The email address and password are sent by the page code so that the 'from' domain can verify that the email address is a legitimate one. If a 'from' address is added or changed in any pages, then the change must be made here as well. If the password of an existing email account is changed, then it must be changed here as well. </p>
    <p>The page is accessible by only users with 'all' access level. </p>
    <p>The page opens with a form that lists all current email addresses. Each field can be changed on the form and the record updated for that person by then clicking on 'Update'.</p>
    <p>A new email address can be added by clicking on the 'Add user' button. A new line appears at the top of the list. Fill in the fields and then click on 'Update.' </p>
    <p>Delete an address by clicking the 'Delete' button. You will be prompted to confirm the deletion or to cancel it. </p>
