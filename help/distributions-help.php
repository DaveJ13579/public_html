    <h2><strong>Harvest distribution help</strong></h2>

    <p>This utility is for recording where crops harvested at a particular harvest went. It is reached from the harvest Update page through the 'Distributions' link. </p>
<p>The page opens with a list of all crop and their weights that were harvested at the indicated harvest. Each crop heading also shows how many pounds of that crop have not been distributed. Another harvest can be selected by entering the new harvest number in the form field and clicking &quot;Display this harvest's distributions&quot;, but it may be best to use the direct link from the harvest Update page to avoid errors.</p>
<p>The date, harvest site and total weight for that harvest are shown.</p>
<p>Under each crop heading are blank rows for entering distributions of portions of that crop. If more rows are needed, saving the distributions using the button at the bottom will add new blank rows. Blank rows will not be added when the total weight of distributions of that crop reaches or exceeds the total of that crop for the harvest. </p>
<p>Enter the date of the distribution. The default is the current date. Change this if needed. Enter pounds of that crop and select the distribution site that those pounds went to from the drop down menu.</p>
<p>Drop down menu items can be selected by clicking and mousing OR by tabbing to the menu and entering the first letters of a menu item until the correct one appears and then tabbing to the next field. Items in the drop down menu are managed using the Distribution Sites Manager linked on the Pages Index.</p>
<p>Click 'Save distributions' when done. </p>
<p>To delete a distribution simply select the blank from the distributions sites drop down menu, or enter zero pounds, and save.</p>
