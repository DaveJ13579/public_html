        <h2><strong>Spot Manager help</strong></h2>
            <p>This page is for adding, changing or removing carpool meeting spots from the spots table.</p>
            <p>The database's spots table is just a list of meeting spots. Each entry in the table has an index identifier for the spot, the name of the spot, and the address. </p>
            <p><strong>Index</strong></p>
            <p>The index is an integer that is used to uniquely identify that spot where it is referred to throughout the website. </p>
            <p><strong>Name</strong></p>
            <p>The name of the meeting spot can be modified but remember that because meeting spots are referred to by their index throughout the site, every carpool of this meeting spot will have the new name if you change it.  So, almost always, changes to the name would be simple corrections or clarifications. </p>
            <p><strong>Address, city, state, zip code</strong></p>
            <p>These are the standard fields for the complete address necesary for Google Maps to determine driving directions. If in doubt, test the address in Google Maps to see if it is interpreted correctly.</p>
            <p><strong>'Add a spot' </strong>button</p>
            <p>Clicking this button inserts a new spot line at the top of the list. Change the name and the address. It will then show up in all dropdown lists of meeting spots on the website.</p>
            <p><strong>'Update'</strong> button</p>
            <p>Click this to commit the changes made to the form fields for that spot.</p>
            <p><strong>'Delete'</strong> button</p>
            <p>A meeting spot can be deleted from the list with this button. Keep in mind that deleting a spot means that any carpools using that spot no longer can access the spot's name or address. It is possible for the web master to restore this spot if the index number and name are known, but it would be <em><strong>very rare</strong></em> to delete a spot from the list. It should happen only if a spot was entered in error, or mistakenly as a duplicate, <em><strong>and there had been no carpool using that spot yet</strong></em>. If you have any doubt about deleting a spot, ask the webmaster for help.</p>
