    <h2><strong>Crop list help</strong></h2>
    <p>This page lists all crops that have been registered. </p>
    <p>The crops that are listed in the main table can be filtered by selecting drop-down lists for 'Venue', Status [All, Active, Inactive], and sorted by [Ripe date, date registered, crop type, crop owner] and then clicking on 'Filter.'</p>
    <p>The first few letters of an owner's last name may be typed in and the list will be filtered for that name.</p>
    <p>As the cursor is moved over crops in the main list, a detail pane appears for each one on the right side. This detail pane has information about the crop and the owner and links to pages for:</p>
<ul>
      <li>Adding a new harvest of this crop to the harvest list and season planner,</li>
      <li>Updating the crop details,</li>
      <li>Showing more details about this crop than are shown in the detail pane, such as the history of harvests of that crop,</li>
      <li>A Google Maps aerial view of the crop. </li>
    </ul>
