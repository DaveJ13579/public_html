    <p>A new user can be added by clicking on the 'Add user' button. A new line appears at the top of the list. Fill in the fields and then click on 'Update.' NOTE: If the user is also a registered volunteer (in the 'pickers' table) then the ID_user field should be the same as the ID_picker in the pickers table. You can look this up by using the Picker Finder form at the bottom. This keeps a couple of pages synched up - particularly the eMailer.</p>
    <p>Delete a user by clicking the 'Delete' button. You will be prompted to confirm the deletion or to cancel it. </p>
