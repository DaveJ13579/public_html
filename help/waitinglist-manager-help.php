        <h2><strong>Waiting List Manager help</strong></h2>
        <p>The Waiting List Manager is linked from the top of the harvest roster page. After signup has begun for a harvest, and the roster has filled and a waiting list begun, there are times it may be useful to be able to easily increase the harvest's picker limit. </p>
        <p>However, doing this just through Harvest Update may open the harvest for more signups, but does not move those on the waiting list into the new roster slots.</p>
        <p>However, this can be done in the Waiting List Manager. As separate steps:</p>
        <p>- The harvest's picker limit can be raised (it cannot be lowered). Enter the new picker limit and click 'Update.'</p>
        <p>- Any number of pickers on the waiting list ('waiters') can be promoted to the roster all at once. The page promotes them in the order in which they were placed on the waiting list. It also sends each of them the same email notification that they would receive if they had been promoted through a cancellation. Enter the number of waiters to be promoted and click 'Promote.'</p>
        <p>- The waiting list for that harvest can be deleted. This should be done only after the harvest is completed (even if it is closed before the harvest), as the action is permanent and it is only when the harvest is completed that the waiting list is no longer needed. Pickers are not notified that the waiting list has been deleted. Click on 'Delete.'</p>
        <p>Care should be taken with all promotions and deletions in the Waiting List Manager as the changes are permanent and unrecoverable.</p>
