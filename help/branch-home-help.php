    <h2><strong>Branch leader home</strong></h2>
    <p>This is the home page for Branch leaders. You arrive here directly after logging in. The page lists harvests that are at sites in your branch in the current year. You can select also select a different year and branch, but there would rarely be a need to do that. </p>
    <p>The green bar at the top of the page has links to:</p>
<blockquote>
	<p><strong>Home</strong>: the home page for Pierce County Gleaning.<br />
		<strong>Branch leader home</strong>: this is the page that you see now.<br />
		<strong>Add a harvest</strong>: the page for adding a new harvest to the harvest list<br />
		<strong>Harvests</strong>: the public page that lists harvests that are available for signing up.</p>
</blockquote>
<p>Each row on this page is a different harvest listed with the latest dates first. You can reverse the list by clicking on the heading of the &quot;Date&quot; column, and reverse it again by clicking again. </p>
    <p>The first column, &quot;Harvest Details,&quot; has a link that takes you to a page that shows all the details about that harvest such as the owner, address and roster. </p>
    <p>The second column, &quot;Harvest Update,&quot; goes to the page for changing details about a harvest and for adding information such as the pounds of crops picked. It also will then let you enter attendance at the harvest.</p>
    <p>The third column, &quot;Site Update,&quot; is used to see all the information about a site and to update it, if necessary.</p>
    <p>The other columns include basic information about the harvest.    </p>
