    <h2><strong>Roster update - attendance help</strong></h2>

    <p>This utility is for entering attendance information from a harvest roster. It uses shortcuts to quickly record the attendance at a harvest.</p>
    <p>Enter the harvest number in the form and click on 'Show records.'</p>
    <p>The picker number, last name, first name, and a form field for attendance will be shown for each person on the roster. The attendance field will have the signup status already in the field. Ideally, the listing in this page will exactly match that on the printed roster submitted by the harvest leader, but if there were late cancellations and promotions from the waiting list, there may be discrepancies. </p>
    <p>Highlight the entry in the status field for the first line. Following the printed roster, enter the attendance for that picker. Shorcuts allow one letter entry:</p>
    <p>'a' = absent<br />
    'i' = intake<br />
    'h' = harvested<br />
    'l' = leader<br />
    'c' = cancel<br />
    <p>After entering one letter for the first roster status entry, the cursor will jump to the next line's status field and highlight the entry there. Pressing another single letter for the attendance allows moving down the page with only one keystroke per line. This means that attendance for even large harvests can be entered quickly. </p>
    <p>To return to the previous line, use the up-arrow key.</p>
<p>After entering all rows, click on 'Submit' or press [Enter]. The full word for each attendance status will be entered into the roster database and redisplayed on the page.</p>
