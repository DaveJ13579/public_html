    <h2><strong>Add a new harvest</strong></h2>
    <p>After finding out about a crop in your branch that is ready to harvest, use this page to add a new harvest to the list. After filling in the details about the harvest, you will click on the &quot;Add new harvest&quot; button. You wil be directed at once to the Harvest Update page where you can change any information or add other  information such as the crops that you are expecting to pick. You must select crops if the page is to be shown on the public Harvests page. </p>
    <p>All new harvests must have at least a site selected. Click on the drop-down menu and either find the site on the list and click on it, or type the first few letters of the site's name and then click to select it. You can also fill in these other fields:</p>
    <p><strong>Leader</strong>: this drop-down list has all of the volunteers who are approved as harvest leaders.<br />
    	<br />
    <strong>Date</strong>: Clicking on the space makes a calendar appear. You can select the day of the harvest from the calendar, or just type it in. Harvest dates must be in &quot;yyyy-mm-dd&quot; format. 
    Note that if you leave the date blank, the page will automatically give the harvest the dummy date &quot;yyyy-00-00&quot; where yyyy is the current year. This will make the harvest appear on your harvest list for the current year. You may want to do this when you know a harvest will happen but you do not yet know the exact date.<br />
    <br />
    <strong>Harvest time</strong>: Select the time of the harvest from the drop-down list.<br />
    <br />
    <strong>Co-leader</strong>: Select a co-leader from the drop-down list if there is one.<br />
    <br />
    <strong>Pickers needed</strong>: This is the total number of volunteers that are needed at the harvest, including the leader and co-leader. <br />
    <br />
    <strong>Duration</strong>: How long the harvest is expected to take. <br />
    <br />
    <strong>Type</strong>: Harvests are categoreized by type. Select from the list.<br />
    <br />
    <strong>Status</strong>:<br />
   <blockquote>
	<strong>closed</strong>: The harvest will be added to the list but it will not appear on the public Harvests page.<br />
    <strong>open</strong>: The harvest will appear on the public harvests page. But it must have a date, a time, a duration and a leader.<br />
<strong>unscheduled</strong>: The harvest is on the list for the year but does not yet have a date.
	</blockquote>
    <strong>Pre-signup info</strong>: This is a short description of the harvest that will appear on the public Harvests page. It should be only information that might be helpful for volunteers to decide to sign up or not.<br />
    <br />
    <strong>Post-signup info</strong>: This information will show up on the page of details that volunteers see after they sign up for the harvest. It might include things like special equipment or directions to the site when it is not clear from Google Maps. 
